package com.sos.jitl.reporting.helper;

public class CounterRemove {

    int triggers;
    int executions;
    int triggerResults;
    int executionDates;

    public int getTriggers() {
        return triggers;
    }

    public void setTriggers(int triggers) {
        this.triggers = triggers;
    }

    public int getExecutions() {
        return executions;
    }

    public void setExecutions(int executions) {
        this.executions = executions;
    }

    public int getTriggerResults() {
        return triggerResults;
    }

    public void setTriggerResults(int triggerResults) {
        this.triggerResults = triggerResults;
    }

    public int getExecutionDates() {
        return executionDates;
    }

    public void setExecutionDates(int executionDates) {
        this.executionDates = executionDates;
    }

}
